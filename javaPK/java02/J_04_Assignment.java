package java02;

public class J_04_Assignment {

	public static void main(String[] args) {
		// (=) Used to assign values to variables

		int a = 13, b = 10;

		System.out.println(a += b); // a = 13 + 10= 23
		System.out.println(a -= b); // a = 23 - 10= 13
		System.out.println(a *= b); // a = 13 * 10= 130
		System.out.println(a /= b); // a = 130 / 10= 13
		System.out.println(a %= b); // a = 13 % 10= 3
	}

}
