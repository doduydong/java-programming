package java09;

public class J_02_Encapsulation {

	private String id;
	private String name;
	private double amount;

	public String getID() {
		return id;
	}

	public void setID(String accID) {
		this.id = accID;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public double getAmount() {
		return amount;
	}

	public void setAmount(double amount) {
		this.amount = amount;
	}

}
